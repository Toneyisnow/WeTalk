// Customize the appearance of table view cells.
- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier] autorelease];
    }
    
	if([indexPath indexAtPosition:0] == 0) {
		// cell.textLabel.text = [NSString stringWithFormat: @"当前用户: %@", [Global getInstance].currentUsername];
	} else {
		if([indexPath indexAtPosition:1] == 0) {
		cell.textLabel.text = @"课程";
		} else {
		cell.textLabel.text = @"设置";
		}
	}
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    
	if([indexPath indexAtPosition:0] == 1) {
		if([indexPath indexAtPosition:1] == 0) {
		LessonListController * cont = [[LessonListController alloc] initWithNibName:@"LessonListController" bundle:nil];
		[self.navigationController pushViewController:cont animated:YES];
		[cont release];
		} else {
		SettingsController * cont = [[SettingsController alloc] initWithNibName:@"SettingsController" bundle:nil];
		[self.navigationController pushViewController:cont animated:YES];
		[cont release];
		}
	}
}



